    private void btn_new_accountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_new_accountActionPerformed
       if (txt_new_pwd.getText().equals(txt_new_pwdcfm.getText())){
            if (x==0){
            try {
            String query;
            query = "INSERT INTO main.passbook (id,uname,pass) VALUES (?,?,?)";
            PreparedStatement pst=newconn.prepareStatement(query);
            pst.setString(1,null);
            pst.setString(2,txt_new_uname.getText());
            pst.setString(3,txt_new_pwd.getText());
            pst.execute();
            pst.close();
            OptionDisplay op=new OptionDisplay();
            op.successcreate();
             txt_new_uname.setText("username");
             txt_new_pwd.setText("sfgerteryry");
             txt_new_pwdcfm.setText("sfgerteryry");
            
        } catch (Exception e) {
            
            OptionDisplay op=new OptionDisplay();
            String error=e.getMessage();
            op.dbFailed(error);
        }
        }
        else{
            OptionDisplay op=new OptionDisplay();
            op.showcustom("User already Exists. Contact Administrator");
            txt_new_uname.setText("username");
             txt_new_pwd.setText("sfgerteryry");
             txt_new_pwdcfm.setText("sfgerteryry");
        }
        
       }
       
       else{
           OptionDisplay op=new OptionDisplay();
           op.pwdWarning("Password Mismatched");
            txt_new_uname.setText("username");
             txt_new_pwd.setText("sfgerteryry");
             txt_new_pwdcfm.setText("sfgerteryry");
       }
    }//GEN-LAST:event_btn_new_accountActionPerformed

