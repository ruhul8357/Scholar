    private void txt_new_unameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_new_unameFocusLost
        String uname= txt_new_uname.getText();
        String query = "select uname from main.passbook where uname='"+uname+"';";
        try {
            PreparedStatement pst = newconn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            boolean empty = true;
            while (rs.next()) {
                empty = false;
                x=1;
            }

            if (empty) {
                x=0;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_txt_new_unameFocusLost

